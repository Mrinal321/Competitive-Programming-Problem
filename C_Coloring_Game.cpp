ll ans(vector<ll> v, ll sum) {
    ll n = v.size(); // in how many ways can we choose 2 elements so that their sum > sum
    ll an = 0;
    ll l = -1;
    for(ll r = 1; r < n; r++) {
        if(l == -1) {
            if(v[r] + v[r-1] > sum) {
                l = r-1;
            }
            else continue;
        }
 
        while(l > 0 && v[l-1] + v[r] > sum) l--;
        an += r - l;
    }
    return an;
}

